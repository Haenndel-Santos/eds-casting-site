    <h2>Overview</h2>
    <p>
      Sand casting is one of the most traditional and flexible foundry processes used to produce metal components 
      of nearly any shape and size. The process uses a sand mold formed around a pattern to shape the cavity into 
      which molten metal is poured. Once solidified, the casting is removed and cleaned to reveal the final part.
    </p>
    <p>
      The technique supports a wide range of materials — including gray and ductile iron, steel, and non-ferrous 
      alloys — and is ideal for medium to large components where mechanical strength, dimensional reliability, 
      and production economy are required. Its adaptability makes it a cornerstone for industrial, automotive, 
      and heavy-equipment applications.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Molding material:</strong> Silica sand or synthetic mixtures bonded with clay or resin systems.</li>
      <li><strong>Alloys:</strong> Gray and ductile irons, steels, bronzes, and aluminum alloys.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT8–CT10 depending on pattern and molding precision.</li>
      <li><strong>Surface finish:</strong> Ra 6.3–12.5 μm; post-finishing or machining often applied.</li>
      <li><strong>Size capability:</strong> From a few kilograms to several tons per piece.</li>
      <li><strong>Production volume:</strong> Suitable for low to medium quantities with affordable tooling cost.</li>
      <li><strong>Molding methods:</strong> Manual green sand, mechanized molding lines, or resin-bonded sand systems.</li>
    </ul>
  </section>

