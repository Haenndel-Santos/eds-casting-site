      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A versatile casting route for large, complex and technically demanding components</h2>
        <p>
          Sand casting is one of the most flexible casting processes for producing industrial metal parts.
          A mould is created from sand around a pattern, molten metal is poured into the mould cavity and
          the component is removed after solidification and cooling.
        </p>
        <p>
          The process is widely used because it can support a broad range of component sizes, geometries,
          materials and production volumes. It is often suitable for parts that are too large, too complex
          or too specialized for more restrictive moulding or die-based processes.
        </p>
        <p>
          EDS helps customers evaluate sand casting feasibility by reviewing geometry, alloy selection,
          wall thickness, tolerances, cores, pattern requirements, machining allowance, inspection needs and
          supplier capability.
        </p>
      </div>

