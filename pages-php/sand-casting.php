        <h2 id="comparison-title">When sand casting may be preferred over other casting routes</h2>
        <p>
          Sand casting is not always the fastest or most dimensionally precise casting process, but it can be
          the most practical route when part size, complexity, material choice or tooling economics are the main drivers.
        </p>
      </div>

      <div class="process-grid-3">
        <article class="process-card">
          <h3>Compared with investment casting</h3>
          <p>
            Sand casting is often better suited for larger parts and lower tooling complexity, while investment
            casting can offer finer detail and tighter as-cast surface quality for smaller precision components.
          </p>
        </article>

        <article class="process-card">
          <h3>Compared with gravity die casting</h3>
          <p>
            Sand casting offers more flexibility for size and material range, while gravity die casting may be
            more efficient for repeatable non-ferrous components with permanent tooling.
          </p>
        </article>

        <article class="process-card">
          <h3>Compared with high-pressure die casting</h3>
          <p>
            Sand casting is more suitable for larger iron or steel components, while high-pressure die casting
            is typically used for high-volume non-ferrous parts with thinner walls.
          </p>
        </article>
