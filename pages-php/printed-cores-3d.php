      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">Additive core production for complex internal casting geometries</h2>
        <p>
          3D core printing is an additive manufacturing process used to produce sand cores directly from
          digital data. Instead of relying on conventional core boxes, the core is built layer by layer using
          sand and binder, allowing complex internal geometries to be produced with greater design freedom.
        </p>
        <p>
          Printed cores can be especially valuable when a casting includes internal channels, cavities, undercuts,
          lightweight structures or flow passages that are difficult or expensive to produce with traditional
          core-making methods.
        </p>
        <p>
          EDS helps customers evaluate 3D printed core feasibility by reviewing casting geometry, internal features,
          core assembly strategy, alloy selection, print constraints, dimensional expectations, handling requirements
          and supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="3D printed cores key benefits">
        <article>
          <h3>Complex Internal Geometry</h3>
          <p>Printed cores can support internal channels, cavities and shapes that are difficult to make conventionally.</p>
        </article>

        <article>
          <h3>Tooling Reduction</h3>
          <p>Core boxes can often be reduced or avoided for prototypes, design validation and selected production needs.</p>
        </article>

        <article>
          <h3>Faster Development</h3>
          <p>Digital production allows faster design changes and more flexible casting development cycles.</p>
        </article>
