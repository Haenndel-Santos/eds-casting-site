    <h2>Overview</h2>
    <p>
      3D Core Printing is a specialized additive manufacturing process used to produce intricate sand cores directly from CAD models,
      eliminating the need for conventional tooling or assembly. Using binder jetting technology, sand particles are selectively 
      bonded layer by layer to create highly detailed and dimensionally accurate internal cores for casting.
    </p>
    <p>
      This process allows the production of lightweight and geometrically complex designs that cannot be achieved through 
      traditional core-making. It is ideal for foundries seeking to shorten development cycles and increase flexibility 
      in both prototype and serial production.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Technology:</strong> Binder jetting of fine sand layers with inorganic or furan-based binders.</li>
      <li><strong>Core size:</strong> From small precision inserts to large multi-part assemblies.</li>
      <li><strong>Dimensional accuracy:</strong> Typically ±0.3 mm depending on geometry and binder type.</li>
      <li><strong>Surface finish:</strong> Ra 6.3–12.5 μm; suitable for direct use in casting molds.</li>
      <li><strong>Materials:</strong> Silica, chromite, or zircon sands compatible with steel, iron, and non-ferrous alloys.</li>
      <li><strong>Production speed:</strong> Rapid build time with no tooling preparation required.</li>
      <li><strong>Integration:</strong> Fully compatible with conventional molding and casting processes.</li>
    </ul>
  </section>

