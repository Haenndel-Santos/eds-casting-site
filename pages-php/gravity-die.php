    <section class="process-section process-intro" id="overview" aria-labelledby="overview-title">
      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A permanent mould casting route for repeatable non-ferrous components</h2>
        <p>
          Gravity die casting uses a reusable metal mould, normally made from steel or cast iron, to produce
          non-ferrous metal components. Molten metal is poured into the die cavity under gravity, where it
          solidifies into a controlled and repeatable component shape.
        </p>
        <p>
          Compared with conventional sand casting, gravity die casting can provide improved dimensional
          stability, better surface finish and more repeatable production quality. It is often used when the
          project volume justifies permanent tooling and the component requires consistent geometry over time.
        </p>
        <p>
          EDS helps customers evaluate gravity die casting feasibility by reviewing alloy selection, part geometry,
          tooling investment, wall thickness, cores, machining allowance, inspection requirements and supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Gravity die casting key benefits">
        <article>
          <h3>Repeatable Production</h3>
          <p>Permanent moulds support consistent geometry and stable production quality across repeated batches.</p>
        </article>

        <article>
          <h3>Good Surface Quality</h3>
          <p>Gravity die casting can provide cleaner surfaces than many conventional sand casting routes.</p>
        </article>

        <article>
          <h3>Tooling Efficiency</h3>
          <p>Reusable dies can support cost-effective production when volumes justify the tooling investment.</p>
        </article>
      </div>
