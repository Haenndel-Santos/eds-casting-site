    <h2>Forging Methods</h2>
    <p>
      The forging process can be carried out using different methods depending on geometry, tolerances, and material requirements.
      EDS works with the main industrial forging techniques:
    </p>
    <ul class="mainpage-list">
      <li><strong>Closed Die Forging:</strong> Metal is compressed within a precisely shaped die cavity for complex geometries.</li>
