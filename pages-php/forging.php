  <!-- HOT FORGING -->
  <section class="mainpage-section alt">
    <h3>Hot Forging</h3>
    <p>
      In hot forging, metals are deformed above their recrystallization temperature (typically 950–1250°C for steel).
      This allows easy plastic deformation, excellent flow characteristics, and the ability to produce large or complex parts.
      Hot forging minimizes internal stress but requires additional machining for dimensional precision.
    </p>
    <ul class="mainpage-list">
      <li>Ideal for large and complex shapes</li>
      <li>Improved metallurgical structure and toughness</li>
      <li>Lower forming forces required</li>
      <li>Common materials: carbon steel, alloy steel, stainless steel</li>
    </ul>
  </section>

  <!-- WARM FORGING -->
  <section class="mainpage-section">
    <h3>Warm Forging</h3>
    <p>
      Warm forging occurs at intermediate temperatures (typically 600–900°C), balancing the advantages of both hot and cold processes.
      It allows better dimensional control than hot forging, while still reducing forming forces compared to cold forging.
    </p>
    <ul class="mainpage-list">
      <li>Improved surface quality and dimensional accuracy</li>
      <li>Reduced tooling wear compared to cold forging</li>
      <li>Suitable for medium-sized parts requiring precise tolerances</li>
    </ul>
  </section>

  <!-- COLD FORGING -->
  <section class="mainpage-section alt">
    <h3>Cold Forging</h3>
    <p>
      Cold forging is performed at or near room temperature, producing parts with excellent surface finish and close tolerances.
      It increases material strength through strain hardening, making it ideal for high-volume production of small precision components.
    </p>
    <ul class="mainpage-list">
      <li>High dimensional accuracy and repeatability</li>
      <li>Excellent surface finish</li>
      <li>Increased material hardness and strength</li>
      <li>Ideal for bolts, shafts, gears, and small precision parts</li>
    </ul>
  </section>

