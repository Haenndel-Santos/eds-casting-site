    <!-- INTRODUCTION -->
    <section class="process-section process-intro" id="overview" aria-labelledby="overview-title">
      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A casting route for integrated shapes, internal features and reduced mould complexity</h2>
        <p>
          Lost foam casting uses an expendable foam pattern that is coated, placed into unbonded sand and
          replaced by molten metal during pouring. As the metal enters the mould, the foam pattern vaporizes
          and the metal fills the space left behind.
        </p>
        <p>
          Because the foam pattern can integrate features that would otherwise require multiple mould parts,
          cores or separate assembly steps, lost foam casting can be useful for complex components with internal
          passages, integrated shapes and reduced parting line requirements.
        </p>
        <p>
          EDS helps customers evaluate lost foam casting feasibility by reviewing geometry, pattern design,
          alloy selection, wall thickness, dimensional expectations, machining allowance, inspection needs and
          supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Lost foam casting key benefits">
        <article>
          <h3>Integrated Geometry</h3>
          <p>Complex shapes and internal features can often be integrated into a single foam pattern.</p>
        </article>

        <article>
          <h3>Reduced Parting Lines</h3>
          <p>The process can reduce traditional mould parting lines and simplify some casting geometries.</p>
        </article>

        <article>
          <h3>Design Flexibility</h3>
          <p>Suitable for selected parts where conventional moulding or core assembly becomes difficult.</p>
        </article>
      </div>
    </section>

