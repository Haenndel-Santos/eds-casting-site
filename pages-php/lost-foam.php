  <!-- OVERVIEW -->
  <section class="mainpage-section mainpage-overview">
    <h2>Overview</h2>
    <p>
      Lost Foam Casting (LFC) is an advanced variation of the evaporative pattern process, where expanded polystyrene (EPS) foam 
      patterns are embedded in unbonded sand and replaced by molten metal during pouring. The foam pattern vaporizes completely, 
      reproducing the exact geometry of the model without the need for cores or parting lines.
    </p>
    <p>
      This technique enables highly accurate and repeatable production of intricate components, particularly beneficial for 
      geometries that are difficult or impossible to achieve through conventional sand casting. The process delivers a high 
      surface finish and dimensional fidelity while minimizing tooling complexity and assembly requirements.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Pattern material:</strong> Expanded polystyrene (EPS) foam, often coated with refractory layer.</li>
      <li><strong>Molding medium:</strong> Unbonded silica sand compacted by vibration around the foam pattern.</li>
      <li><strong>Alloys:</strong> Aluminum, gray and ductile iron, steel, and non-ferrous alloys.</li>
      <li><strong>Tolerances:</strong> ISO 8062 CT6–CT8 depending on alloy and part size.</li>
      <li><strong>Surface finish:</strong> Ra 3.2–6.3 μm; smoother than green sand casting.</li>
      <li><strong>Pattern tooling:</strong> Low-cost aluminum tooling for foam pattern shaping.</li>
      <li><strong>Applications:</strong> Ideal for prototypes and medium production volumes of complex castings.</li>
    </ul>
  </section>

  <!-- MAIN APPLICATIONS -->
