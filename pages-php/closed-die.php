        </article>

        <article>
          <h3>Mechanical Integrity</h3>
          <p>Controlled deformation supports improved grain flow, strength and fatigue performance.</p>
        </article>

        <article>
          <h3>Efficient Production</h3>
          <p>Near-net shapes can reduce machining time, material waste and total production complexity.</p>
        </article>
