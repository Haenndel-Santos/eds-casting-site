  <section class="mainpage-section mainpage-overview">
    <h2>Overview</h2>
    <p>
      Closed Die Forging, also known as Impression Die Forging, is a controlled process in which heated metal is placed 
      between two dies that contain the desired shape of the final component. As pressure is applied, the material 
      flows and fills the die cavity, resulting in a dense and dimensionally accurate product.
    </p>
    <p>
      This forging technique enhances mechanical strength, grain flow orientation, and impact resistance, making it 
      ideal for critical parts that require structural integrity and fatigue performance. The process is widely 
      used across industries including automotive, railway, construction, and heavy machinery.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Material types:</strong> Carbon steels, alloy steels, stainless steels, copper and aluminum alloys.</li>
      <li><strong>Forming temperature:</strong> Typically 950–1250°C for steels; 400–500°C for non-ferrous alloys.</li>
      <li><strong>Equipment:</strong> Mechanical or hydraulic presses with controlled dies.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT5–CT7 depending on die precision.</li>
      <li><strong>Surface finish:</strong> Ra 3.2–6.3 μm after forging; machining optional.</li>
      <li><strong>Weight range:</strong> From a few hundred grams to several hundred kilograms per part.</li>
      <li><strong>Grain structure:</strong> Optimized through controlled deformation and flow direction.</li>
    </ul>
  </section>

