      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A forging route for controlled shapes, repeatability and structural performance</h2>
        <p>
          Closed die forging, also known as impression die forging, shapes heated metal between dies that
          contain a defined cavity. As the material is compressed, it flows into the die geometry and forms
          a part that is close to the desired final shape.
        </p>
        <p>
          This process is often selected for components that need strong mechanical properties, controlled
          grain flow, repeatable production quality and better material utilization compared with machining
          a part from solid stock.
        </p>
        <p>
          EDS helps customers evaluate whether closed die forging is the right choice by reviewing geometry,
          material grade, tolerances, production volume, tooling investment, machining allowance and quality
          documentation requirements.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Closed die forging key benefits">
