    <p>
      Multi-Directional Forging is an advanced forming process that applies force from multiple directions simultaneously,
      allowing the material to flow uniformly into complex die cavities. This technique improves structural integrity,
      mechanical properties, and dimensional precision, making it ideal for components that cannot be efficiently
      produced by conventional single-direction forging.
    </p>
    <p>
      By optimizing deformation from several axes, the process ensures refined grain structures,
      minimal material waste, and superior internal soundness — key features for parts operating
      under high stress, such as in the automotive, energy, and aerospace sectors.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Forces applied:</strong> Simultaneous multi-axis compression improving isotropic properties.</li>
      <li><strong>Equipment:</strong> Specialized hydraulic or mechanical presses with multi-ram systems.</li>
      <li><strong>Materials:</strong> Carbon, alloy, and stainless steels, nickel-based and titanium alloys.</li>
      <li><strong>Forming temperature:</strong> Typically 900–1200°C depending on material and section thickness.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT5–CT7 depending on tooling precision.</li>
      <li><strong>Grain structure:</strong> Refined and homogeneous, enhancing fatigue and impact resistance.</li>
      <li><strong>Surface finish:</strong> Ra 3.2–6.3 μm before machining.</li>
    </ul>
  </section>

