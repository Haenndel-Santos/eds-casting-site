    <section class="process-section process-intro" id="overview" aria-labelledby="overview-title">
      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">Forging from multiple directions to improve material flow and component performance</h2>
        <p>
          Multi-directional forging is a controlled forming process in which the material is compressed from
          different directions to produce components with complex geometry, improved material distribution and
          enhanced mechanical properties. Instead of relying on a single forming direction, the process uses
          multiple deformation paths to guide material flow into critical areas of the part.
        </p>
        <p>
          This process can be especially useful when a component has branches, ribs, bosses, hubs, transitions
          or load paths that require stronger material continuity than a simpler forging route may provide.
          It can help reduce weak zones, improve grain orientation and support more reliable performance in
          demanding operating conditions.
        </p>
        <p>
          EDS helps customers evaluate whether multi-directional forging is technically and commercially suitable
          by reviewing component geometry, material grade, load direction, tooling complexity, machining allowance,
          production volume and supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Multi-directional forging key benefits">
