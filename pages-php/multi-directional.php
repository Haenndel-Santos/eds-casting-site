        </article>

        <article>
          <h3>Improved Material Flow</h3>
          <p>Multiple forming directions can improve grain orientation and strengthen critical load paths.</p>
        </article>

        <article>
          <h3>Performance Reliability</h3>
          <p>Useful for parts exposed to high loads, fatigue, impact or demanding structural requirements.</p>
        </article>
