      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A precision sand-based casting route for repeatable components and improved surface quality</h2>
        <p>
          Shell moulding is a casting process that uses a thin shell mould made from resin-coated sand.
          The sand mixture is applied to a heated metal pattern, where it cures and forms a rigid mould
          shell. After curing, the shell halves are assembled and molten metal is poured into the cavity.
        </p>
        <p>
          Compared with conventional sand casting, shell moulding can provide improved dimensional accuracy,
          better surface finish and stronger repeatability. It is often selected when the component requires
          more control than standard sand casting, but does not require the investment or production volume
          associated with permanent die processes.
        </p>
        <p>
          EDS helps customers evaluate shell moulding feasibility by reviewing geometry, alloy selection,
          tolerance expectations, shell tooling, production volume, machining allowance, inspection needs and
          supplier capability.
        </p>
      </div>

