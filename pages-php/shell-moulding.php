  <!-- OVERVIEW -->
  <section class="mainpage-section mainpage-overview">
    <h2>Overview</h2>
    <p>
      Shell moulding casting is a semi-permanent mould process that uses resin-coated sand to form a thin, 
      rigid shell around a heated metal pattern. Once the shell hardens, the halves are assembled and 
      filled with molten metal to produce highly accurate castings with excellent surface quality.
    </p>
    <p>
      The process bridges the gap between traditional sand casting and high-precision methods like die casting, 
      delivering improved repeatability, finer details, and tighter tolerances, while maintaining flexibility 
      for small-to-medium production series.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Mould material:</strong> Silica sand coated with thermosetting resin (phenolic or epoxy-based).</li>
      <li><strong>Pattern:</strong> Heated metal pattern used to cure and form the sand-resin shell.</li>
      <li><strong>Wall thickness:</strong> Typically 5–10 mm per shell, ensuring dimensional consistency.</li>
      <li><strong>Alloys:</strong> Iron, steel, stainless steel, and various non-ferrous alloys.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT6–CT7 with smooth surface finish (Ra 3.2–6.3 μm).</li>
      <li><strong>Production range:</strong> Suitable for small to medium series with complex geometries.</li>
      <li><strong>Post-processing:</strong> Standard machining and surface treatments when required.</li>
    </ul>
  </section>

  <!-- MAIN APPLICATIONS -->
  <section class="mainpage-section">
    <h2>Main Applications</h2>
