    </p>
    <ul class="mainpage-list">
      <li>Automotive parts such as brackets, manifolds, pulleys, and housings</li>
      <li>Pump, valve, and compressor components with intricate flow paths</li>
      <li>Gear housings and precision covers in mechanical assemblies</li>
      <li>Electrical and instrumentation enclosures</li>
      <li>Medium-complex industrial castings with tight tolerances</li>
    </ul>
