      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">Additive sand mould and core production for faster casting development</h2>
        <p>
          3D sand printing is an additive manufacturing process used to produce sand moulds and cores directly
          from digital data. Instead of manufacturing a conventional pattern or core box, the mould or core is
          built layer by layer using sand and binder.
        </p>
        <p>
          This process can be valuable when casting geometry is complex, development time is limited or tooling
          investment is difficult to justify. It allows engineers to test casting concepts, internal channels,
          optimized cores and design changes without the same tooling constraints found in traditional mould making.
        </p>
        <p>
          EDS helps customers evaluate 3D sand printing feasibility by reviewing casting geometry, mould design,
          core strategy, alloy selection, print constraints, dimensional expectations, finishing requirements and
          supplier capability.
        </p>
      </div>

