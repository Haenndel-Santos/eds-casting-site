    <h2>Overview</h2>
    <p>
      Ring Rolled Forging is a controlled metal forming process used to produce seamless rings with excellent structural integrity and
      mechanical performance. The process begins with an open-die preform, which is then heated and rolled between two dies—one 
      driving and one idle—expanding the diameter while reducing wall thickness.
    </p>
    <p>
      This results in a refined grain flow that follows the ring’s circular profile, enhancing strength, impact resistance, and fatigue
      properties. Rolled rings are widely used in industries where durability and precision under dynamic loads are critical.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Materials:</strong> Carbon steels, alloy steels, stainless steels, titanium, aluminum, and nickel-based alloys.</li>
      <li><strong>Forming temperature:</strong> Typically 950–1200°C for steels and 400–500°C for non-ferrous materials.</li>
      <li><strong>Diameter range:</strong> From 100 mm up to over 6 meters, depending on ring rolling capacity.</li>
      <li><strong>Wall thickness:</strong> Controlled to tight tolerances with uniform distribution.</li>
      <li><strong>Grain structure:</strong> Continuous circumferential flow for superior fatigue and impact resistance.</li>
      <li><strong>Weight range:</strong> From a few kilograms to several tons per ring.</li>
      <li><strong>Post-processing:</strong> Heat treatment, machining, and surface finishing as per specification.</li>
    </ul>
  </section>

