    <section class="process-section process-intro" id="overview" aria-labelledby="overview-title">
      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">Seamless forged rings with circumferential grain flow and high mechanical reliability</h2>
        <p>
          Rolled ring forging is a controlled metal forming process used to produce seamless rings from a
          forged preform. The heated preform is pierced and then expanded between rolling tools, increasing
          the ring diameter while reducing wall thickness and shaping the final ring profile.
        </p>
        <p>
          The process creates a continuous circumferential grain flow, which can improve strength, fatigue
          resistance and structural performance compared with welded or machined-from-plate alternatives.
          This makes rolled rings suitable for demanding applications where circular integrity is important.
        </p>
        <p>
          EDS helps customers evaluate rolled ring forging feasibility by reviewing material grade, diameter,
          wall thickness, cross-section, machining allowance, heat treatment, inspection needs and supplier capacity.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Rolled ring forging key benefits">
        <article>
          <h3>Seamless Integrity</h3>
          <p>Ring rolling avoids welded joints and supports continuous material structure around the circumference.</p>
        </article>

        <article>
          <h3>Controlled Grain Flow</h3>
          <p>The forging process aligns grain flow around the ring, supporting strength and fatigue performance.</p>
        </article>

        <article>
          <h3>Efficient Ring Production</h3>
          <p>Near-net ring shapes can reduce machining stock, material waste and total manufacturing effort.</p>
        </article>
      </div>
