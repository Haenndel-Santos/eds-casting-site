      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">Flexible forging for large parts, simple geometries and demanding mechanical performance</h2>
        <p>
          Open die forging is a metal forming process in which the workpiece is compressed between flat
          or simple shaped dies without being fully enclosed in a die cavity. The material is progressively
          shaped by controlled deformation, often through repeated pressing, hammering, rotating and repositioning.
        </p>
        <p>
          This process is particularly useful for large components, lower-volume production, custom sizes
          and parts that require strong internal material structure. It is often selected when flexibility,
          mechanical integrity and material refinement are more important than producing a highly detailed
          near-net shape.
        </p>
        <p>
          EDS helps customers evaluate whether open die forging is suitable by reviewing part dimensions,
          alloy requirements, mechanical properties, machining allowance, heat treatment, production volume
          and supplier capability.
        </p>
      </div>

