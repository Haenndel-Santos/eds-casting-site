    <h2>Overview</h2>
    <p>
      Open Die Forging, also known as Free Forging, is a deformation process where heated metal is shaped 
      between flat or contoured dies that do not completely enclose the material. This method allows 
      the production of large components with superior internal integrity, excellent grain structure, 
      and unmatched mechanical strength.
    </p>
    <p>
      The process is highly flexible and suitable for manufacturing a wide range of parts — from simple 
      shafts and rings to custom heavy forgings for energy, transportation, and industrial equipment.
      The controlled deformation refines the grain flow and eliminates internal voids, resulting in
      high-quality, structurally sound forgings.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Equipment:</strong> Hydraulic or pneumatic hammers, presses, and manipulators for heavy billets.</li>
      <li><strong>Materials:</strong> Carbon steels, alloy steels, stainless steels, nickel-based and titanium alloys.</li>
      <li><strong>Forming temperature:</strong> Typically 950–1250°C for steels; precise control ensures optimal flow.</li>
      <li><strong>Component size:</strong> From 10 kg up to several tons per piece.</li>
      <li><strong>Dimensional range:</strong> Up to 1.5 meters in diameter or several meters in length, depending on press capacity.</li>
      <li><strong>Grain refinement:</strong> Continuous deformation enhances fatigue and impact resistance.</li>
      <li><strong>Applications:</strong> Ideal for large components requiring superior toughness and reliability.</li>
    </ul>
  </section>

