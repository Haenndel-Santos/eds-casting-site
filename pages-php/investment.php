    <section class="process-section process-intro" id="overview" aria-labelledby="overview-title">
      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A casting route for complex geometry, fine detail and near-net shape production</h2>
        <p>
          Investment casting, also known as lost wax casting, is a precision casting process used to produce
          complex metal components with fine detail, good surface finish and reduced machining requirements.
          A wax pattern is created, coated with ceramic material and then removed to form a mould cavity for molten metal.
        </p>
        <p>
          This process is often selected when component geometry is too complex for conventional machining or
          when the project requires thin walls, internal features, accurate detail and repeatable dimensional control.
          It can be suitable for both small technical parts and more demanding industrial components.
        </p>
        <p>
          EDS helps customers evaluate investment casting feasibility by reviewing part geometry, alloy selection,
          tolerance expectations, surface requirements, tooling, machining allowance, inspection needs and supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="Investment casting key benefits">
        <article>
          <h3>Precision Geometry</h3>
          <p>Suitable for complex shapes, fine details, thin walls and features that are difficult to machine efficiently.</p>
        </article>

        <article>
          <h3>Reduced Machining</h3>
          <p>Near-net shape production can reduce material removal, machining time and downstream cost.</p>
        </article>

        <article>
          <h3>Material Versatility</h3>
          <p>Supports a wide range of steels and alloys depending on application, strength and corrosion requirements.</p>
        </article>
      </div>
