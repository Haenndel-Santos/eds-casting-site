      <li>Power transmission and drivetrain parts (geometrically complex brackets and yokes)</li>
      <li>Industrial machinery, tooling components, and precision brackets</li>
      <li>Energy, marine, and general engineering components requiring corrosion/heat resistance</li>
      <li>Hardware with aesthetic surfaces where minimal machining is desired</li>
    </ul>
