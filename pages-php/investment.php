    <h2>Overview</h2>
    <p>
      Investment casting (lost-wax casting) uses expendable wax patterns assembled into trees, coated with 
      ceramic slurry, and fired to form a high-precision mold. Molten metal is poured into the ceramic shell, 
      producing parts with tight tolerances, thin walls, and refined detail that often require minimal machining.
    </p>
    <p>
      The process is highly versatile across ferrous and non-ferrous alloys, making it ideal for complex 
      components where dimensional accuracy, repeatability, and surface quality are critical.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Pattern:</strong> Wax patterns assembled into trees; ceramic shell built by dipping and stuccoing.</li>
      <li><strong>Alloys:</strong> Carbon & alloy steels, stainless steels, duplex, tool steels, nickel & cobalt alloys, aluminum, bronze.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT4–CT6 depending on geometry and alloy.</li>
      <li><strong>Surface finish:</strong> Typically Ra 1.6–3.2 μm; excellent detail and logo/marking definition.</li>
      <li><strong>Wall thickness:</strong> As low as 2–3 mm (geometry-dependent); sharp fillets and fine features feasible.</li>
      <li><strong>Part size:</strong> From a few grams up to several kilograms per casting (project-specific).</li>
      <li><strong>Post-operations:</strong> Heat treatment, HIP (when required), CNC machining, NDT (RT/UT/DP/MP), coatings.</li>
    </ul>
  </section>

  <!-- MAIN APPLICATIONS -->
  <section class="mainpage-section">
    <h2>Main Applications</h2>
    <p>
