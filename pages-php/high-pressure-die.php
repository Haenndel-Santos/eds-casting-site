    <h2>Overview</h2>
    <p>
      High Pressure Die Casting (HPDC) is a manufacturing process that injects molten metal under high pressure
      into a hardened steel mould, producing parts with exceptional precision, surface finish, and repeatability.
      It is one of the fastest and most economical casting methods for large-scale production of non-ferrous components,
      especially aluminium, zinc, and magnesium alloys.
    </p>
    <p>
      The HPDC process is ideal for parts with thin walls, tight tolerances, and complex geometries,
      where both strength and dimensional stability are essential. The result is a near-net-shape product
      that requires minimal machining and delivers consistent quality over long production runs.
    </p>
  </section>

  <!-- PROCESS CHARACTERISTICS -->
  <section class="mainpage-section alt">
    <h2>Process Characteristics</h2>
    <ul class="mainpage-list">
      <li><strong>Injection pressure:</strong> Typically between 700 and 1,200 bar depending on alloy and part size.</li>
      <li><strong>Alloys:</strong> Aluminium (AlSi9Cu3, AlSi10Mg), Zinc (Zamak), and Magnesium.</li>
      <li><strong>Mould type:</strong> Hardened steel dies with automated lubrication and cooling systems.</li>
      <li><strong>Cycle time:</strong> 30 to 90 seconds, ideal for high-volume production.</li>
      <li><strong>Dimensional accuracy:</strong> ISO 8062 CT5–CT6, allowing extremely tight tolerances.</li>
      <li><strong>Surface finish:</strong> Ra 1.6–3.2 μm, often suitable for direct painting or coating.</li>
    </ul>
  </section>

