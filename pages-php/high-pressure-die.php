      <div class="process-intro__text">
        <p class="process-eyebrow">Process Overview</p>
        <h2 id="overview-title">A fast and repeatable casting route for precise non-ferrous parts</h2>
        <p>
          High-pressure die casting is a permanent mould casting process in which molten non-ferrous metal
          is injected into a steel die at high speed and pressure. The process is widely used for components
          requiring thin walls, good dimensional repeatability, high production efficiency and consistent detail.
        </p>
        <p>
          Because the process uses dedicated tooling and fast cycle times, it is typically selected for medium
          to high-volume production where the tooling investment can be justified by repeatability, productivity
          and lower unit cost over the production life of the component.
        </p>
        <p>
          EDS helps customers evaluate high-pressure die casting feasibility by reviewing alloy selection,
          part geometry, wall thickness, die design, production volume, machining needs, surface requirements,
          inspection expectations and supplier capability.
        </p>
      </div>

      <div class="process-intro__cards" aria-label="High-pressure die casting key benefits">
